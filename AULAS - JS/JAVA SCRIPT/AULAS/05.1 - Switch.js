// O switch-case trabalha com situações mutuamente exclusivas //
let estrelas = 6



switch (estrelas) {
    
    case 1:
        console.log('Insatisfeito!!😒');
    break;


    case 2:
        console.log('Ruim!!🤨');
    break;


    case 3:
        console.log('Rasoavel!!🙄');
    break;


    case 4:
        console.log('BOM!!😁');
    break;


    case 5:
        console.log('Maravilhoso!!!!!!😍');
        break;

    default:
        console.log('Invalidado! Se quiser Valide 😄');
        break;

        
} 


