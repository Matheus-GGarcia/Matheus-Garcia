// O switch-case trabalha com situações mutuamente exclusivas //


let sabor = "Nutelicia"

switch (sabor){
 
    case "Nutelicia":
        console.log('Seu Sorvete Está Pronto:', sabor);

        break;
    case "Céu Azul":
        console.log('Seu Sorvete Está Pronto:', sabor);

        break;
    case "Chocolate":
        console.log('Seu Sorvete Está Pronto:', sabor);

        break;

    default:
            console.log('Desculpe não temos esse sabor');
        break;
}

 

      



        
                

